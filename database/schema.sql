-- 1. Create Tables
----------------------------------------------------------

-- Table: CASES
CREATE TABLE CASES (
    CASE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    INDUSTRY VARCHAR2(50),
    SCENARIO_TYPE VARCHAR2(50),
    NAME VARCHAR2(100),
    ATTRIBUTE_1 VARCHAR2(200),
    ATTRIBUTE_2 VARCHAR2(200),
    ATTRIBUTE_3 VARCHAR2(200),
    ATTRIBUTE_4 VARCHAR2(200),
    IS_FRAUD CHAR(1),
    EXPLANATION VARCHAR2(1000)
);

-- Table: PLAYERS
CREATE TABLE PLAYERS (
    PLAYER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(100),
    COMPANY VARCHAR2(100),
    INDUSTRY VARCHAR2(50),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Table: LEADERBOARD
CREATE TABLE LEADERBOARD (
    ENTRY_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PLAYER_ID NUMBER,
    SCORE NUMBER,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    TOTAL_TIME NUMBER,
    PLAYER_TYPE VARCHAR2(10) DEFAULT 'HUMAN', -- 'HUMAN' or 'AI'
    SESSION_ID VARCHAR2(50)
);

-- Table: PLAYER_CASE_RESULTS
CREATE TABLE PLAYER_CASE_RESULTS (
    RESULT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PLAYER_ID NUMBER,
    CASE_ID NUMBER,
    SELECTED_ANSWER CHAR(1),
    IS_CORRECT CHAR(1),
    CONFIDENCE VARCHAR2(10),
    RESPONSE_TIME NUMBER,
    BASE_SCORE NUMBER,
    SPEED_BONUS NUMBER,
    CONFIDENCE_BONUS NUMBER,
    TOTAL_SCORE NUMBER,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    ACTOR_TYPE VARCHAR2(10) DEFAULT 'HUMAN' -- 'HUMAN' or 'AI'
);
